public class EnhanchedForLoop {
    public static void main(String args[]){
        
       //vars
       int[] numbers = {100, -50, 1000, 25000, 1000000, -550, 10000000, -50000, -10, 754125, -1, -250000, -1000000, -50, 0};
       
       //loop
       for(int num : numbers){
           System.out.print(num + " ");
       }
       
    }
}